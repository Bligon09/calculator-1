"""Functions for common math operations."""


def add(num1, num2):
    total_add= num1+num2
    return total_add


def subtract(num1, num2):
    minus_total= num1-num2
    return minus_total

def multiply(num1, num2):
    multi_total= num1*num2
    return multi_total


def divide(num1, num2):
    divide_total=num1/num2
    return divide_total

def square(num1):
    square_total=num1*num1
    return square_total


def cube(num1):
    cube_total= num1**3
    return cube_total


def power(num1, num2):
    pow_total=num1**num2
    return pow_total


def mod(num1, num2):
    mod_total=num1%num2
    return mod_total
